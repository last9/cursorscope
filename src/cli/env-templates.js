import { LAST9_OTEL_ADMIN_NOTE, LAST9_OTEL_INTEGRATION_URL } from "./last9.js";

/** @typedef {{ otlpBase?: string, otlpHeaders?: string }} Last9EnvOptions */

/** @param {string} home @param {string} version @param {Last9EnvOptions} [opts] */
export function buildLast9Env(home, version = "0.0.0", opts = {}) {
  const base = normalizeOtlpBase(opts.otlpBase || "https://otlp-aps1.last9.io");
  const headers =
    opts.otlpHeaders?.trim() || "Authorization=Basic REPLACE_WITH_LAST9_OTLP_TOKEN";

  return `# Generated by: cursorscope setup --last9
PORT=8787
CURSORSCOPE_AUTO_START=true
CURSORSCOPE_HOME=${home}

OTEL_EXPORTER_OTLP_TRACES_ENDPOINT=${base}/v1/traces
OTEL_EXPORTER_OTLP_METRICS_ENDPOINT=${base}/v1/metrics
OTEL_EXPORTER_OTLP_LOGS_ENDPOINT=${base}/v1/logs
# Copy from ${LAST9_OTEL_INTEGRATION_URL}
# ${LAST9_OTEL_ADMIN_NOTE}
OTEL_EXPORTER_OTLP_HEADERS=${headers}
OTEL_EXPORTER_OTLP_METRICS_TEMPORALITY_PREFERENCE=cumulative

OTEL_SERVICE_NAME=cursorscope
OTEL_SERVICE_NAMESPACE=cursorscope
OTEL_SERVICE_VERSION=${version}
DEPLOYMENT_ENVIRONMENT=local
DEBUG_OTEL=false

CURSOR_LOG_USER_PROMPTS=false
CURSOR_LOG_TOOL_DETAILS=false
CURSOR_MASK_USER_EMAIL=false
CURSOR_TRACK_ATTRIBUTED_TOKENS=true

ENABLE_CURSOR_API_POLLING=false
CURSOR_API_BASE_URL=https://api.cursor.com
CURSOR_ADMIN_API_KEY=
CURSOR_TEAM_ID=
CURSOR_API_POLL_INTERVAL_MS=300000

CURSOR_HOOK_ENDPOINT=http://localhost:8787/cursor/hooks
`;
}

/** @param {string} url */
export function normalizeOtlpBase(url) {
  return url.trim().replace(/\/+$/, "");
}
